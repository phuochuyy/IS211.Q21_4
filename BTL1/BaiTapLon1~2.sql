-- 1) Trigger: Kiểm tra và cập nhật tông kho khi thêm chi tiết đơn hàng
CREATE OR REPLACE TRIGGER MAY2.TRG_CAPNHAT_KHO_CTDH_CN02
BEFORE INSERT ON MAY2.CT_DONHANG_CN02
FOR EACH ROW
DECLARE
    V_SOLUONGTON MAY2.KHO_CN02_TON.SOLUONGTON%TYPE;
BEGIN
    IF :NEW.MACN <> 'CN02' THEN
        RAISE_APPLICATION_ERROR(-20020, 'Chi tiet don hang CN02 chi duoc co MACN = CN02');
    END IF;

    SELECT SOLUONGTON
    INTO V_SOLUONGTON
    FROM MAY2.KHO_CN02_TON
    WHERE MACN = :NEW.MACN
      AND MABT = :NEW.MABT
    FOR UPDATE;

    IF :NEW.SOLUONG > V_SOLUONGTON THEN
        RAISE_APPLICATION_ERROR(-20021, 'So luong ton kho CN02 khong du');
    END IF;

    UPDATE MAY2.KHO_CN02_TON
    SET SOLUONGTON = SOLUONGTON - :NEW.SOLUONG
    WHERE MACN = :NEW.MACN
      AND MABT = :NEW.MABT;

    UPDATE MAY2.KHO_CN02_BANHANG
    SET SOLUONGDABAN = SOLUONGDABAN + :NEW.SOLUONG,
        NGAYCAPNHAT = SYSDATE
    WHERE MACN = :NEW.MACN
      AND MABT = :NEW.MABT;

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20022, 'Khong tim thay bien the san pham trong kho CN02');
END;
/
