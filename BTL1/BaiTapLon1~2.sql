--Câu 4: Top 10 biến thể sản phẩm bán chạy nhất toàn hệ thống-MAY2
SELECT 
    CT.MABT,
    BT.MASP,
    BT.MAU,
    BT.KICHCO,
    COUNT(*) AS SO_LAN_XUAT_HIEN,
    SUM(CT.SOLUONG) AS TONG_SO_LUONG_BAN
FROM (
    SELECT MABT, SOLUONG
    FROM CT_DONHANG_CN01@LINK_MAY2_TO_MAY1

    UNION ALL

    SELECT MABT, SOLUONG
    FROM MAY2.CT_DONHANG_CN02

    UNION ALL

    SELECT MABT, SOLUONG
    FROM CT_DONHANG_CN03@LINK_MAY2_TO_MAY3

    UNION ALL

    SELECT MABT, SOLUONG
    FROM CT_DONHANG_CN04@LINK_MAY2_TO_MAY4
) CT
JOIN MAY2.BIENTHE_SANPHAM BT
    ON CT.MABT = BT.MABT
GROUP BY CT.MABT, BT.MASP, BT.MAU, BT.KICHCO
ORDER BY TONG_SO_LUONG_BAN DESC
FETCH FIRST 10 ROWS ONLY;
--Câu 5: Top 10 khách hàng có tổng chi tiêu cao nhất toàn hệ thống- MAY2
SELECT 
    KH.MAKH,
    KH.HOTEN,
    KH.SODIENTHOAI,
    KH.EMAIL,
    TV.HANGTV,
    TV.DIEMTICHLUY,
    COUNT(DH.MADH) AS SO_DON_DA_MUA,
    SUM(DH.TONGTHANHTOAN) AS TONG_CHI_TIEU
FROM KHACHHANG_TT@LINK_MAY2_TO_MAY1 KH
JOIN MAY2.KHACHHANG_THANHVIEN TV
    ON KH.MAKH = TV.MAKH
JOIN (
    SELECT MADH, MAKH, TONGTHANHTOAN
    FROM DONHANG_CN01@LINK_MAY2_TO_MAY1

    UNION ALL

    SELECT MADH, MAKH, TONGTHANHTOAN
    FROM MAY2.DONHANG_CN02

    UNION ALL

    SELECT MADH, MAKH, TONGTHANHTOAN
    FROM DONHANG_CN03@LINK_MAY2_TO_MAY3

    UNION ALL

    SELECT MADH, MAKH, TONGTHANHTOAN
    FROM DONHANG_CN04@LINK_MAY2_TO_MAY4
) DH
    ON KH.MAKH = DH.MAKH
GROUP BY 
    KH.MAKH,
    KH.HOTEN,
    KH.SODIENTHOAI,
    KH.EMAIL,
    TV.HANGTV,
    TV.DIEMTICHLUY
ORDER BY TONG_CHI_TIEU DESC
FETCH FIRST 10 ROWS ONLY;
--Câu 6: Thống kê doanh thu theo phương thức thanh toán toàn hệ thống-MAY2
SELECT 
    PHUONGTHUC,
    COUNT(*) AS SO_GIAO_DICH,
    SUM(SOTIEN) AS TONG_DOANH_THU,
    ROUND(AVG(SOTIEN), 0) AS GIA_TRI_THANH_TOAN_TB
