BEGIN
    IF :NEW.MACN <> 'CN01' THEN
        RAISE_APPLICATION_ERROR(-20010, 'Chi tiet don hang CN01 chi duoc co MACN = CN01');
    END IF;

    SELECT SOLUONGTON
    INTO V_SOLUONGTON
    FROM MAY1.KHO_CN01_TON
    WHERE MACN = :NEW.MACN
      AND MABT = :NEW.MABT
    FOR UPDATE;

    IF :NEW.SOLUONG > V_SOLUONGTON THEN
        RAISE_APPLICATION_ERROR(-20011, 'So luong ton kho CN01 khong du');
    END IF;

    UPDATE MAY1.KHO_CN01_TON
    SET SOLUONGTON = SOLUONGTON - :NEW.SOLUONG
    WHERE MACN = :NEW.MACN
      AND MABT = :NEW.MABT;

    UPDATE MAY1.KHO_CN01_BANHANG
    SET SOLUONGDABAN = SOLUONGDABAN + :NEW.SOLUONG,
        NGAYCAPNHAT = SYSDATE
    WHERE MACN = :NEW.MACN
      AND MABT = :NEW.MABT;

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20012, 'Khong tim thay bien the san pham trong kho CN01');
END;
