CREATE OR REPLACE PROCEDURE MAY1.PR_CAPNHAT_LUONG_NHANVIEN_TOANHT (
    P_MANV      IN VARCHAR2,
    P_LUONG_MOI IN NUMBER
)
IS
    V_COUNT NUMBER := 0;
    V_LUONG_CU NUMBER := 0;
BEGIN
    -- 1. Kiem tra luong moi hop le
    IF P_LUONG_MOI <= 0 THEN
        RAISE_APPLICATION_ERROR(-20001, 'Luong moi phai lon hon 0');
    END IF;

    -- 2. Tim nhan vien tai MAY1 - CN01
    SELECT COUNT(*)
    INTO V_COUNT
    FROM MAY1.NHANVIEN_CN01
    WHERE MANV = P_MANV;

    IF V_COUNT > 0 THEN
        SELECT LUONG
        INTO V_LUONG_CU
        FROM MAY1.NHANVIEN_CN01
        WHERE MANV = P_MANV;

        IF P_LUONG_MOI <= V_LUONG_CU THEN
            RAISE_APPLICATION_ERROR(-20002, 'Luong moi phai lon hon luong hien tai');
        END IF;

        UPDATE MAY1.NHANVIEN_CN01
        SET LUONG = P_LUONG_MOI
        WHERE MANV = P_MANV;

        COMMIT;
        RETURN;
    END IF;

