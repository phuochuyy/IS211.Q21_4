--Câu 1: Thống kê tổng quan đơn hàng theo từng chi nhánh- MAY1
SELECT 
    MACN,
    COUNT(*) AS SO_DON_HANG,
    SUM(TONGTHANHTOAN) AS TONG_DOANH_THU,
    ROUND(AVG(TONGTHANHTOAN), 0) AS GIA_TRI_DON_TRUNG_BINH
FROM (
    SELECT MACN, TONGTHANHTOAN
    FROM MAY1.DONHANG_CN01

    UNION ALL

    SELECT MACN, TONGTHANHTOAN
    FROM DONHANG_CN02@LINK_MAY1_TO_MAY2

    UNION ALL

    SELECT MACN, TONGTHANHTOAN
    FROM DONHANG_CN03@LINK_MAY1_TO_MAY3

    UNION ALL

    SELECT MACN, TONGTHANHTOAN
    FROM DONHANG_CN04@LINK_MAY1_TO_MAY4
)
GROUP BY MACN
ORDER BY MACN

--Câu 2: Thống kê doanh thu theo trạng thái đơn hàng toàn hệ thống-MAY1
