        SET LUONG = P_LUONG_MOI
        WHERE MANV = P_MANV;

        COMMIT;
        RETURN;
    END IF;

    -- 4. Tim nhan vien tai MAY3 - CN03
    SELECT COUNT(*)
    INTO V_COUNT
    FROM NHANVIEN_CN03@LINK_MAY1_TO_MAY3
    WHERE MANV = P_MANV;

    IF V_COUNT > 0 THEN
        SELECT LUONG
        INTO V_LUONG_CU
        FROM NHANVIEN_CN03@LINK_MAY1_TO_MAY3
        WHERE MANV = P_MANV;

        IF P_LUONG_MOI <= V_LUONG_CU THEN
            RAISE_APPLICATION_ERROR(-20002, 'Luong moi phai lon hon luong hien tai');
        END IF;

        UPDATE NHANVIEN_CN03@LINK_MAY1_TO_MAY3
        SET LUONG = P_LUONG_MOI
        WHERE MANV = P_MANV;

        COMMIT;
        RETURN;
    END IF;

    -- 5. Tim nhan vien tai MAY4 - CN04
    SELECT COUNT(*)
    INTO V_COUNT
    FROM NHANVIEN_CN04@LINK_MAY1_TO_MAY4
    WHERE MANV = P_MANV;

