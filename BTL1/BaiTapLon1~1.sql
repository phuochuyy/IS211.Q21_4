        SELECT LUONG
        INTO V_LUONG_CU
        FROM NHANVIEN_CN04@LINK_MAY1_TO_MAY4
        WHERE MANV = P_MANV;

        IF P_LUONG_MOI <= V_LUONG_CU THEN
            RAISE_APPLICATION_ERROR(-20002, 'Luong moi phai lon hon luong hien tai');
        END IF;

        UPDATE NHANVIEN_CN04@LINK_MAY1_TO_MAY4
        SET LUONG = P_LUONG_MOI
        WHERE MANV = P_MANV;

        COMMIT;
        RETURN;
    END IF;

    -- 6. Neu khong tim thay nhan vien trong 4 site
    RAISE_APPLICATION_ERROR(-20003, 'Khong tim thay nhan vien trong toan he thong');

EXCEPTION
