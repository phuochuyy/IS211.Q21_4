CREATE OR REPLACE FUNCTION MAY1.FN_TONG_CHI_TIEU_KHACHHANG (
    P_MAKH IN VARCHAR2
)
RETURN NUMBER
IS
    V_TONG_CN01 NUMBER := 0;
    V_TONG_CN02 NUMBER := 0;
    V_TONG_CN03 NUMBER := 0;
    V_TONG_CN04 NUMBER := 0;
    V_TONG_TOAN_HE_THONG NUMBER := 0;
BEGIN
    -- 1. Tinh tong chi tieu tai chi nhanh CN01 - MAY1
    SELECT NVL(SUM(TONGTHANHTOAN), 0)
    INTO V_TONG_CN01
    FROM MAY1.DONHANG_CN01
    WHERE MAKH = P_MAKH;

    -- 2. Tinh tong chi tieu tai chi nhanh CN02 - MAY2
    SELECT NVL(SUM(TONGTHANHTOAN), 0)
    INTO V_TONG_CN02
    FROM DONHANG_CN02@LINK_MAY1_TO_MAY2
    WHERE MAKH = P_MAKH;

    -- 3. Tinh tong chi tieu tai chi nhanh CN03 - MAY3
    SELECT NVL(SUM(TONGTHANHTOAN), 0)
    INTO V_TONG_CN03
    FROM DONHANG_CN03@LINK_MAY1_TO_MAY3
    WHERE MAKH = P_MAKH;

    -- 4. Tinh tong chi tieu tai chi nhanh CN04 - MAY4
    SELECT NVL(SUM(TONGTHANHTOAN), 0)
    INTO V_TONG_CN04
    FROM DONHANG_CN04@LINK_MAY1_TO_MAY4
    WHERE MAKH = P_MAKH;

    -- 5. Cong tong chi tieu toan he thong
    V_TONG_TOAN_HE_THONG := V_TONG_CN01 + V_TONG_CN02 + V_TONG_CN03 + V_TONG_CN04;

    RETURN V_TONG_TOAN_HE_THONG;

EXCEPTION
    WHEN OTHERS THEN
        RAISE_APPLICATION_ERROR(
            -20001,
            'Loi khi tinh tong chi tieu khach hang tren he thong phan tan: ' || SQLERRM
        );
END;
