-- 1) Trigger: Kiểm tra và cập nhật tông kho khi thêm chi tiết đơn hàng
CREATE OR REPLACE TRIGGER MAY4.TRG_CAPNHAT_KHO_CTDH_CN04
BEFORE INSERT ON MAY4.CT_DONHANG_CN04
FOR EACH ROW
DECLARE
    V_SOLUONGTON MAY4.KHO_CN04_TON.SOLUONGTON%TYPE;
BEGIN
    IF :NEW.MACN <> 'CN04' THEN
        RAISE_APPLICATION_ERROR(-20040, 'Chi tiet don hang CN04 chi duoc co MACN = CN04');
    END IF;

    SELECT SOLUONGTON
    INTO V_SOLUONGTON
    FROM MAY4.KHO_CN04_TON
    WHERE MACN = :NEW.MACN
      AND MABT = :NEW.MABT
    FOR UPDATE;

    IF :NEW.SOLUONG > V_SOLUONGTON THEN
        RAISE_APPLICATION_ERROR(-20041, 'So luong ton kho CN04 khong du');
    END IF;

    UPDATE MAY4.KHO_CN04_TON
    SET SOLUONGTON = SOLUONGTON - :NEW.SOLUONG
    WHERE MACN = :NEW.MACN
      AND MABT = :NEW.MABT;

    UPDATE MAY4.KHO_CN04_BANHANG
    SET SOLUONGDABAN = SOLUONGDABAN + :NEW.SOLUONG,
        NGAYCAPNHAT = SYSDATE
    WHERE MACN = :NEW.MACN
      AND MABT = :NEW.MABT;

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20042, 'Khong tim thay bien the san pham trong kho CN04');
END;
