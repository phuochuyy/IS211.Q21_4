    SELECT MABT
    FROM CT_DONHANG_CN01@LINK_MAY4_TO_MAY1

    INTERSECT

    SELECT MABT
    FROM CT_DONHANG_CN02@LINK_MAY4_TO_MAY2

    INTERSECT

    SELECT MABT
    FROM CT_DONHANG_CN03@LINK_MAY4_TO_MAY3

    INTERSECT

    SELECT MABT
    FROM MAY4.CT_DONHANG_CN04
) X
    ON BT.MABT = X.MABT
ORDER BY BT.MABT
FETCH FIRST 10 ROWS ONLY;
--Câu 10: Tìm 10 khách hàng đã mua hàng ở đủ cả 4 chi nhánh- MAY4
SELECT 
    KH.MAKH,
    KH.HOTEN,
    KH.SODIENTHOAI,
    KH.EMAIL,
    COUNT(DISTINCT DH.MACN) AS SO_CHI_NHANH_DA_MUA
FROM KHACHHANG_TT@LINK_MAY4_TO_MAY1 KH
JOIN (
    SELECT MAKH, MACN
    FROM DONHANG_CN01@LINK_MAY4_TO_MAY1

    UNION ALL

    SELECT MAKH, MACN
    FROM DONHANG_CN02@LINK_MAY4_TO_MAY2

    UNION ALL

    SELECT MAKH, MACN
    FROM DONHANG_CN03@LINK_MAY4_TO_MAY3

    UNION ALL

    SELECT MAKH, MACN
    FROM MAY4.DONHANG_CN04
) DH
    ON KH.MAKH = DH.MAKH
GROUP BY 
    KH.MAKH,
    KH.HOTEN,
    KH.SODIENTHOAI,
    KH.EMAIL
HAVING COUNT(DISTINCT DH.MACN) = 4
ORDER BY KH.MAKH
FETCH FIRST 10 ROWS ONLY;
